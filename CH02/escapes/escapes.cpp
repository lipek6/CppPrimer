/*
 * newline \n horizontal tab \t alert (bell) \a
 * vertical tab \v backspace \b double quote \"
 * backslash \\ question mark \? single quote \'
 * carriage return \r formfeed \f 
*/

#include <iostream>

int main()
{
    std::cout << "newline \n" << std::endl << std::endl;
    std::cout << "horizontal tab \t aaa" << std::endl << std::endl;
    std::cout << "alert (bell) \a aaa" << std::endl << std::endl;
    std::cout << "vertical tab \v aaa" << std::endl << std::endl;
    std::cout << "backspace \b aaa" << std::endl << std::endl;
    std::cout << "double quote \" aaa" << std::endl << std::endl;
    std::cout << "backslash \\ aaa" << std::endl << std::endl;
    std::cout << "question mark \? aaa" << std::endl << std::endl;
    std::cout << "single quote \' aaa" << std::endl << std::endl;
    std::cout << "carriage return \r aaa" << std::endl << std::endl;
    std::cout << "formfeed \f aaa" << std::endl << std::endl;
}